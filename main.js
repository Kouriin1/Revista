    // ==========================================
    // FUNCIONES: ARTICULO INDIVIDUAL
    // ==========================================
    function loadArticlePage() {
        // Obtener ID de la URL
        const params = new URLSearchParams(window.location.search);
        const articleId = params.get('id');

        if (!articleId || !articlesData[articleId]) {
            document.querySelector('.article-container').innerHTML = "<h2>Artículo no encontrado.</h2><a href='index.html'>Volver al inicio</a>";
            return;
        }

        const data = articlesData[articleId];

        // Inyectar contenido
        document.getElementById('article-title').textContent = data.title;
        document.getElementById('article-author').textContent = data.author;
        document.getElementById('article-affiliation').textContent = data.affiliation;
        document.getElementById('article-abstract').textContent = data.abstract;
        document.getElementById('article-body').innerHTML = data.body;
        
        // Referencias
        const refList = document.getElementById('article-references-list');
        data.references.forEach(ref => {
            const li = document.createElement('li');
            li.textContent = ref;
            refList.appendChild(li);
        });

        // Cambiar título de la pestaña
        document.title = `${data.title} | Revista TIC`;

        // Scroll Progress Bar
        window.addEventListener('scroll', () => {
            const winScroll = document.body.scrollTop || document.documentElement.scrollTop;
            const height = document.documentElement.scrollHeight - document.documentElement.clientHeight;
            const scrolled = (winScroll / height) * 100;
            const progressBar = document.getElementById("progressBar");
            if(progressBar) progressBar.style.width = scrolled + "%";
        });
    }

    // ==========================================
    // EFECTOS COMUNES
    // ==========================================
    function initVisualEffects() {
        // 1. Sticky Navbar
        const navbar = document.getElementById('navbar');
        if (navbar) {
            window.addEventListener('scroll', () => {
                if (window.scrollY > 50) {
                    navbar.classList.add('scrolled');
                } else {
                    navbar.classList.remove('scrolled');
                }
            });
        }

        // 2. Scroll Reveal
        const observerOptions = {
            root: null,
            rootMargin: '0px',
            threshold: 0.1 
        };

        const observer = new IntersectionObserver((entries, observer) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.classList.add('active');
                    observer.unobserve(entry.target);
                }
            });
        }, observerOptions);

        const cards = document.querySelectorAll('.scroll-reveal');
        cards.forEach(card => observer.observe(card));

        // 3. Smooth Scroll Links
        document.querySelectorAll('a[href^="#"]').forEach(anchor => {
            anchor.addEventListener('click', function (e) {
                const targetId = this.getAttribute('href');
                if (targetId === '#') return;
                
                // Solo si el target existe en esta página (para no romper links externos/internos)
                const target = document.querySelector(targetId);
                if (target) {
                    e.preventDefault();
                    target.scrollIntoView({ behavior: 'smooth' });
                }
            });
        });
    }
